### Logical Operators

#1. and
print(True and True)
print(True and False)

#2. or
print(True or True)
print(False or False)

#3.not
print(not True)
print(not 10)

#Falsy values = '' , 0 , None, False, blank (all data structure)
print(not '') 
print(not 0)
print(not None)
print(not False)
print(not [])
print(not {})
print(not ())