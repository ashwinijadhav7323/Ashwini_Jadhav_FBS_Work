### Program to find quotient and remainder of two numbers.

#Take two numbers.
num1 = int(input("Enter First Number : "))
num2 = int(input("Enter Second Number : "))

#Perform Operation.
quotient = num1 // num2
remainder = num1 % num2

#Display Result
print(f'The Quotient is {quotient} & the remainder is {remainder}.')